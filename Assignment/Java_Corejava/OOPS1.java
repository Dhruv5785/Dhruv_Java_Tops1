package Assignment_work;

public class OOPS1 {
   public void printn(int a) {
	   System.out.println(a);
   }
   
   public void printn(float b) {
	   System.out.println(b);
   }
   
   public void printn(long c) {
	   System.out.println(c);
   }
   
   public void printn(double d) {
	   System.out.println(d);
   }
   
   
  public static void main(String[] args) {
	     OOPS1 ab=new OOPS1();
	     ab.printn(10);
	     ab.printn(10.39);
	     ab.printn(28828828);
	     ab.printn(238.39939939);
  }
}
